export function Sidebar() {
  const navigate = useNavigate();
  const location = useLocation();
  const [isOpen, setIsOpen] = useState(false);

  // Hàm xử lý logout
  const handleLogout = () => {
    localStorage.removeItem('user');
    navigate('/');
    setIsOpen(false);
  };

  // Kiểm tra active link
  const isActive = (path) => {
    if (path === '/dashboard' && location.pathname === '/dashboard') return true;
    if (path !== '/dashboard' && location.pathname === path) return true;
    return false;
  };

  return (
    <>
      <button
        type="button"
        className={`sidebar-toggle ${isOpen ? 'open' : ''}`}
        onClick={() => setIsOpen((prev) => !prev)}
        aria-label={isOpen ? 'Thu gọn menu' : 'Mở menu'}
      >
        <Icons.menu />
      </button>

      {isOpen && <div className="sidebar-backdrop" onClick={() => setIsOpen(false)}></div>}

      <aside className={`sidebar ${isOpen ? 'open' : ''}`}>
      <div className="brand">
        <div className="logo">
          <Icons.pickleball />
        </div>
        <div className="title">
          Alpha Pickleball
          <br />
          <small>Trang quản trị</small>
        </div>
      </div>

      <nav className="snav" aria-label="Chính">
        <Link className={`slink ${isActive('/') ? 'active' : ''}`} to="/" onClick={() => setIsOpen(false)}>
          <span className="ic"><Icons.home /></span>
          <span>Trang chủ</span>
        </Link>
        <Link className={`slink ${isActive('/dashboard') ? 'active' : ''}`} to="/dashboard" onClick={() => setIsOpen(false)}>
          <span className="ic"><Icons.dashboard /></span>
          <span>Thống kê</span>
        </Link>
        <Link className={`slink ${isActive('/dat-san') ? 'active' : ''}`} to="/dat-san" onClick={() => setIsOpen(false)}>
          <span className="ic"><Icons.calendar /></span>
          <span>Đặt sân ngày</span>
        </Link>
        <Link className={`slink ${isActive('/santhang') ? 'active' : ''}`} to="/santhang" onClick={() => setIsOpen(false)}>
          <span className="ic"><Icons.calendarMonth /></span>
          <span>Đặt sân tháng</span>
        </Link>
        <Link className={`slink ${isActive('/quanlysan') ? 'active' : ''}`} to="/quanlysan" onClick={() => setIsOpen(false)}>
          <span className="ic"><Icons.pickleball /></span>
          <span>Quản lý sân</span>
        </Link>
      </nav>

      <div className="divider"></div>

      <nav className="bottom" aria-label="Hỗ trợ">
        {/* Logout */}
        <span className="slink" onClick={handleLogout} style={{ cursor: 'pointer' }}>
          <span className="ic"><Icons.logout /></span>
          <span>Đăng xuất</span>
        </span>
      </nav>
      </aside>
    </>
  );
}
